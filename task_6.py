distance_meters = 1000
meters_to_inches = distance_meters / 0.0254
meters_to_feet = distance_meters / 0.3048
meters_to_yards = distance_meters / 0.9144
meters_to_miles = distance_meters / 1609.34
print(f"Inches: {meters_to_inches:.2f}")
print(f"Feet: {meters_to_feet:.2f}")
print(f"Yards: {meters_to_yards:.2f}")
print(f"Miles: {meters_to_miles:.2f}")